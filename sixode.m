function dydx = sixode(x,y,PTILDE)
dydx = [ - (2*y(2,:).*(y(6,:) + y(2,:).^2 + (y(4,:).^(-1)) .* (2*y(2,:) + (y(4,:).^(-1)) .* cos(y(3,:))).*cos(y(3,:))) + PTILDE).*sin(x);...
			(y(4,:).^(-2)) .* y(1,:) .* (sin(x)) ;...
			y(4,:).^(-1) .* (2*y(2,:) + y(4,:).^(-1) .* cos(y(3,:))).*sin(x);...
			y(4,:).^(-1) .* sin(y(3,:)) .* sin(x);...
			y(4,:).^(-1) .* cos(y(3,:)) .* sin(x);...
			x*0];
end